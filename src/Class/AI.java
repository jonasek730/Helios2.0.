package Class;

/**
 * Třída s parametry pro komentáře spojené s pomocí při hře
 */
public class AI {
    private String name;
    private String taskStart;
    private String taskHangar;
    private String taskLaboratory;
    private String taskEnd;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTaskStart() {
        return taskStart;
    }

    public void setTaskStart(String taskStart) {
        this.taskStart = taskStart;
    }

    public String getTaskHangar() {
        return taskHangar;
    }

    public void setTaskHangar(String taskHangar) {
        this.taskHangar = taskHangar;
    }

    public String getTaskLaboratory() {
        return taskLaboratory;
    }

    public void setTaskLaboratory(String taskLaboratory) {
        this.taskLaboratory = taskLaboratory;
    }

    public String getTaskEnd() {
        return taskEnd;
    }

    public void setTaskEnd(String taskEnd) {
        this.taskEnd = taskEnd;
    }
}
